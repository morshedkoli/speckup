write_stub() {
  local path="$1"
  local note="$2"
  if [ ! -f "$path" ]; then
    if [ -n "$note" ]; then
      echo "$note" > "$path"
      echo "// TODO: implement" >> "$path"
    else
      echo "// TODO: implement" > "$path"
    fi
  fi
}

for item in "${PLACEHOLDERS[@]}"; do
  IFS='|' read -r path note <<<"$item"
  write_stub "$path" "$note"
done

for item in "${FEATURE_FILES[@]}"; do
  IFS='|' read -r path note <<<"$item"
  write_stub "$path" "$note"
